/* ──────────────────────────────────────────────────────────────────────── */

int ext_star_count(void)
{
    return CATALOG_SIZE;
}

ext_star_t ext_star_get(int index)
{
    ext_star_t s;
    if (index < 0 || index >= CATALOG_SIZE) {
        s.ra_hours = 0.0f;
        s.dec_deg  = 0.0f;
        s.v_mag    = 99.0f;
        s.bv       = 0.0f;
        return s;
    }
    s.ra_hours = cat_ra[index];
    s.dec_deg  = cat_dec[index];
    s.v_mag    = cat_vmag[index];
    s.bv       = cat_bv[index];
    return s;
}

const char *ext_star_name(int index)
{
    if (index < 0 || index >= CATALOG_SIZE) return NULL;
    return cat_names[index];
}

int ext_star_tier_count(int tier)
{
    if (tier < 0 || tier >= EXT_TIER_COUNT) return 0;
    return tier_count_arr[tier];
}

int ext_star_tier_start(int tier)
{
    if (tier < 0 || tier >= EXT_TIER_COUNT) return 0;
    return tier_start[tier];
}

int ext_star_tier_for_mag(float v_mag)
{
    if (v_mag < EXT_TIER_0_MAG) return 0;
    if (v_mag < EXT_TIER_1_MAG) return 1;
    if (v_mag < EXT_TIER_2_MAG) return 2;
    return 3;
}

int ext_star_named_count(void)
{
    int count = 0;
    for (int i = 0; i < CATALOG_SIZE; i++) {
        if (cat_names[i] != NULL) count++;
    }
    return count;
}

int ext_star_find(const char *name)
{
    if (name == NULL) return -1;
    for (int i = 0; i < CATALOG_SIZE; i++) {
        if (cat_names[i] != NULL && strcasecmp(cat_names[i], name) == 0) {
            return i;
        }
    }
    return -1;
}

int ext_star_in_region(float ra_min, float ra_max,
                       float dec_min, float dec_max,
                       int *out, int max_out)
{
    if (out == NULL || max_out <= 0) return 0;

    int count = 0;
    int wrap = (ra_min > ra_max); /* RA wraps around 0/24 */

    for (int i = 0; i < CATALOG_SIZE && count < max_out; i++) {
        float ra = cat_ra[i];
        float dec = cat_dec[i];

        /* Dec check */
        if (dec < dec_min || dec > dec_max) continue;

        /* RA check with wrap-around */
        if (wrap) {
            if (ra < ra_min && ra > ra_max) continue;
        } else {
            if (ra < ra_min || ra > ra_max) continue;
        }

        out[count++] = i;
    }
    return count;
}
