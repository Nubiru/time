#include "noise_shader.h"

static const char *s_noise_source =
    /* --- Permutation helpers (no texture lookups) --- */
    "vec3 mod289_3(vec3 x) { return x - floor(x * (1.0/289.0)) * 289.0; }\n"
    "vec4 mod289_4(vec4 x) { return x - floor(x * (1.0/289.0)) * 289.0; }\n"
    "vec3 permute3(vec3 x) { return mod289_3(((x*34.0)+1.0)*x); }\n"
    "vec4 permute(vec4 x) { return mod289_4(((x*34.0)+1.0)*x); }\n"
    "vec4 taylorInvSqrt(vec4 r) { return 1.79284291400159 - 0.85373472095314 * r; }\n"
    "\n"

    /* --- 3D Simplex Noise --- */
    "float snoise3(vec3 v) {\n"
    "    const vec2 C = vec2(1.0/6.0, 1.0/3.0);\n"
    "    const vec4 D = vec4(0.0, 0.5, 1.0, 2.0);\n"
    "    vec3 i = floor(v + dot(v, C.yyy));\n"
    "    vec3 x0 = v - i + dot(i, C.xxx);\n"
    "    vec3 g = step(x0.yzx, x0.xyz);\n"
    "    vec3 l = 1.0 - g;\n"
    "    vec3 i1 = min(g.xyz, l.zxy);\n"
    "    vec3 i2 = max(g.xyz, l.zxy);\n"
    "    vec3 x1 = x0 - i1 + C.xxx;\n"
    "    vec3 x2 = x0 - i2 + C.yyy;\n"
    "    vec3 x3 = x0 - D.yyy;\n"
    "    i = mod289_3(i);\n"
    "    vec4 p = permute(permute(permute(\n"
    "        i.z + vec4(0.0, i1.z, i2.z, 1.0))\n"
    "      + i.y + vec4(0.0, i1.y, i2.y, 1.0))\n"
    "      + i.x + vec4(0.0, i1.x, i2.x, 1.0));\n"
    "    float n_ = 0.142857142857;\n"
    "    vec3 ns = n_ * D.wyz - D.xzx;\n"
    "    vec4 j = p - 49.0 * floor(p * ns.z * ns.z);\n"
    "    vec4 x_ = floor(j * ns.z);\n"
    "    vec4 y_ = floor(j - 7.0 * x_);\n"
    "    vec4 x = x_ * ns.x + ns.yyyy;\n"
    "    vec4 y = y_ * ns.x + ns.yyyy;\n"
    "    vec4 h = 1.0 - abs(x) - abs(y);\n"
    "    vec4 b0 = vec4(x.xy, y.xy);\n"
    "    vec4 b1 = vec4(x.zw, y.zw);\n"
    "    vec4 s0 = floor(b0)*2.0 + 1.0;\n"
    "    vec4 s1 = floor(b1)*2.0 + 1.0;\n"
    "    vec4 sh = -step(h, vec4(0.0));\n"
    "    vec4 a0 = b0.xzyw + s0.xzyw*sh.xxyy;\n"
    "    vec4 a1 = b1.xzyw + s1.xzyw*sh.zzww;\n"
    "    vec3 p0 = vec3(a0.xy, h.x);\n"
    "    vec3 p1 = vec3(a0.zw, h.y);\n"
    "    vec3 p2 = vec3(a1.xy, h.z);\n"
    "    vec3 p3 = vec3(a1.zw, h.w);\n"
    "    vec4 norm = taylorInvSqrt(vec4(dot(p0,p0), dot(p1,p1), dot(p2,p2), dot(p3,p3)));\n"
    "    p0 *= norm.x; p1 *= norm.y; p2 *= norm.z; p3 *= norm.w;\n"
    "    vec4 m = max(0.6 - vec4(dot(x0,x0), dot(x1,x1), dot(x2,x2), dot(x3,x3)), 0.0);\n"
    "    m = m * m;\n"
    "    return 42.0 * dot(m*m, vec4(dot(p0,x0), dot(p1,x1), dot(p2,x2), dot(p3,x3)));\n"
    "}\n"
    "\n"

    /* --- 2D Simplex Noise (lighter weight) --- */
    "float snoise2(vec2 v) {\n"
    "    const vec4 C = vec4(0.211324865405187, 0.366025403784439,\n"
    "                       -0.577350269189626, 0.024390243902439);\n"
    "    vec2 i = floor(v + dot(v, C.yy));\n"
    "    vec2 x0 = v - i + dot(i, C.xx);\n"
    "    vec2 i1 = (x0.x > x0.y) ? vec2(1.0, 0.0) : vec2(0.0, 1.0);\n"
    "    vec4 x12 = x0.xyxy + C.xxzz;\n"
    "    x12.xy -= i1;\n"
    "    i = mod289_3(vec3(i, 0.0)).xy;\n"
    "    vec3 pm = permute3(permute3(i.y + vec3(0.0, i1.y, 1.0))\n"
    "                             + i.x + vec3(0.0, i1.x, 1.0));\n"
    "    vec3 m = max(0.5 - vec3(dot(x0,x0), dot(x12.xy,x12.xy), dot(x12.zw,x12.zw)), 0.0);\n"
    "    m = m*m; m = m*m;\n"
    "    vec3 x = 2.0 * fract(pm * C.www) - 1.0;\n"
    "    vec3 h = abs(x) - 0.5;\n"
    "    vec3 ox = floor(x + 0.5);\n"
    "    vec3 a0 = x - ox;\n"
    "    m *= 1.79284291400159 - 0.85373472095314 * (a0*a0 + h*h);\n"
    "    vec3 gv;\n"
    "    gv.x = a0.x * x0.x + h.x * x0.y;\n"
    "    gv.yz = a0.yz * x12.xz + h.yz * x12.yw;\n"
    "    return 130.0 * dot(m, gv);\n"
    "}\n"
    "\n"

    /* --- Fractal Brownian Motion (3D, 5 octaves) --- */
    "float fbm3(vec3 p) {\n"
    "    float f = 0.0;\n"
    "    float amp = 0.5;\n"
    "    for (int i = 0; i < 5; i++) {\n"
    "        f += amp * snoise3(p);\n"
    "        p *= 2.03;\n"
    "        amp *= 0.5;\n"
    "    }\n"
    "    return f;\n"
    "}\n"
    "\n"

    /* --- Fractal Brownian Motion (2D, 5 octaves) --- */
    "float fbm2(vec2 p) {\n"
    "    float f = 0.0;\n"
    "    float amp = 0.5;\n"
    "    for (int i = 0; i < 5; i++) {\n"
    "        f += amp * snoise2(p);\n"
    "        p *= 2.03;\n"
    "        amp *= 0.5;\n"
    "    }\n"
    "    return f;\n"
    "}\n"
    "\n"

    /* --- Value noise (cheaper than simplex, for detail/dust layers) --- */
    "float hash21(vec2 p) {\n"
    "    vec3 p3 = fract(vec3(p.xyx) * 0.1031);\n"
    "    p3 += dot(p3, p3.yzx + 33.33);\n"
    "    return fract((p3.x + p3.y) * p3.z);\n"
    "}\n"
    "\n"
    "float vnoise(vec2 p) {\n"
    "    vec2 i = floor(p);\n"
    "    vec2 f = fract(p);\n"
    "    f = f * f * (3.0 - 2.0 * f);\n"
    "    float a = hash21(i);\n"
    "    float b = hash21(i + vec2(1.0, 0.0));\n"
    "    float c = hash21(i + vec2(0.0, 1.0));\n"
    "    float d = hash21(i + vec2(1.0, 1.0));\n"
    "    return mix(mix(a, b, f.x), mix(c, d, f.x), f.y);\n"
    "}\n"
    "\n"
    "float vfbm(vec2 p) {\n"
    "    float f = 0.0;\n"
    "    float amp = 0.5;\n"
    "    for (int i = 0; i < 4; i++) {\n"
    "        f += amp * vnoise(p);\n"
    "        p *= 2.03;\n"
    "        amp *= 0.5;\n"
    "    }\n"
    "    return f;\n"
    "}\n";

const char *noise_shader_source(void) { return s_noise_source; }
