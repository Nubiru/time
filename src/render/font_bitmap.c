int font_bitmap_atlas_width(void) {
    return FONT_BITMAP_ATLAS_W;
}

int font_bitmap_atlas_height(void) {
    return FONT_BITMAP_ATLAS_H;
}

int font_bitmap_atlas_size(void) {
    return FONT_BITMAP_ATLAS_W * FONT_BITMAP_ATLAS_H;
}

int font_bitmap_glyph_count(void) {
    return FONT_BITMAP_COUNT;
}

const uint8_t *font_bitmap_glyph_data(char c) {
    int code = (unsigned char)c;
    if (code < FONT_BITMAP_FIRST || code > FONT_BITMAP_LAST) {
        return NULL;
    }
    return s_glyphs[code - FONT_BITMAP_FIRST];
}

int font_bitmap_generate(uint8_t *out_pixels) {
    if (out_pixels == NULL) {
        return -1;
    }

    memset(out_pixels, 0, (size_t)(FONT_BITMAP_ATLAS_W * FONT_BITMAP_ATLAS_H));

    for (int c = FONT_BITMAP_FIRST; c <= FONT_BITMAP_LAST; c++) {
        int idx = c - FONT_BITMAP_FIRST;
        int grid_col = idx % FONT_BITMAP_COLS;
        int grid_row = idx / FONT_BITMAP_COLS;
        int px = grid_col * FONT_BITMAP_CELL_W;
        int py = grid_row * FONT_BITMAP_CELL_H;

        /* Center the 5x7 glyph in the 8x16 cell */
        int offset_x = 1;
        int offset_y = 4;

        const uint8_t *glyph = s_glyphs[idx];

        for (int row = 0; row < FONT_BITMAP_GLYPH_H; row++) {
            uint8_t bits = glyph[row];
            for (int col = 0; col < FONT_BITMAP_GLYPH_W; col++) {
                if (bits & (0x80 >> col)) {
                    int dest_x = px + offset_x + col;
                    int dest_y = py + offset_y + row;
                    out_pixels[dest_y * FONT_BITMAP_ATLAS_W + dest_x] = 0xFF;
                }
            }
        }
    }

    return 0;
}
